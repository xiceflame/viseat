def expand_synonyms(name: str) -> list:
    """扩展食材同义词，返回所有可能的名称"""
    name_lower = name.lower().strip()
    results = [name, name_lower]
    
    for canonical, synonyms in DISH_SYNONYMS.items():
        all_forms = [canonical.lower()] + [s.lower() for s in synonyms]
        if name_lower in all_forms:
            results.extend([canonical] + synonyms)
            break
    
    return list(set(results))

def get_canonical_name(name: str) -> str:
    """获取食材的标准名称"""
    name_lower = name.lower().strip()
    
    for canonical, synonyms in DISH_SYNONYMS.items():
        all_forms = [canonical.lower()] + [s.lower() for s in synonyms]
        if name_lower in all_forms:
            return canonical
    
    return name  # 未找到则返回原名


class FoodMapper:
    """食材快速映射器 - 结合预定义表和完整营养数据库"""
    
    def __init__(self):
        self._cache: Dict[str, FoodMapping] = {}
        self._nutrition_db = None  # 延迟加载
    
    def _get_nutrition_db(self):
        """延迟加载营养数据库"""
        if self._nutrition_db is None:
            from .nutrition_db import get_nutrition_db
            self._nutrition_db = get_nutrition_db()
        return self._nutrition_db
    
    def lookup(self, name: str, category: Optional[str] = None) -> FoodMapping:
        """
        快速查找食材营养数据
        
        查找顺序：
        1. 缓存命中（最快）
        2. 预定义表精确匹配（常见 300+ 食材）
        3. 营养数据库查询（6000+ 食材）
        4. 预定义表关键词匹配
        5. 分类默认值
        6. 通用默认值
        
        Args:
            name: 食材名（VLM 返回的）
            category: 分类（用于兜底）
            
        Returns:
            FoodMapping 对象
        """
        # 标准化名称
        key = name.lower().strip()
        
        # 1. 缓存命中
        if key in self._cache:
            return self._cache[key]
        
        # 2. 预定义表精确匹配（最快路径）
        if key in FOOD_ALIASES:
            db_key, cal, pro, carb, fat = FOOD_ALIASES[key]
            result = FoodMapping(db_key, cal, pro, carb, fat, 1.0)
            self._cache[key] = result
            return result
        
        # 3. 中文名精确匹配
        if name in FOOD_ALIASES:
            db_key, cal, pro, carb, fat = FOOD_ALIASES[name]
            result = FoodMapping(db_key, cal, pro, carb, fat, 1.0)
            self._cache[key] = result
            return result
        
        # 4. 快餐/外卖映射（500+ 种）
        if name in _FASTFOOD_MAPPINGS:
            info = _FASTFOOD_MAPPINGS[name]
            result = FoodMapping(
                name,
                info.get('calories', 150),
                info.get('protein', 5),
                info.get('carbs', 20),
                info.get('fat', 5),
                0.85 if info.get('source') == 'database' else 0.7
            )
            self._cache[key] = result
            return result
        
        # 5. LLM 生成的食材映射（6000+ 别名）
        if key in _GENERATED_MAPPINGS:
            info = _GENERATED_MAPPINGS[key]
            db_key = info.get('db_key', name)
            # 从 nutrition_db 获取准确营养值
            try:
                db = self._get_nutrition_db()
                nutrition, is_exact = db.lookup(db_key)
                if nutrition.source.value not in ("fallback", "category_default"):
                    result = FoodMapping(
                        db_key,
                        nutrition.energy_kcal,
                        nutrition.protein,
                        nutrition.carbohydrate,
                        nutrition.fat,
                        0.9 if is_exact else 0.75
                    )
                    self._cache[key] = result
                    return result
            except Exception:
                pass
        
        # 5. 查询完整营养数据库（6000+ 食材）
        try:
            db = self._get_nutrition_db()
            nutrition, is_exact = db.lookup(name, category=category)
            # 只有非 fallback 结果才使用
            if nutrition.source.value not in ("fallback", "category_default"):
                result = FoodMapping(
                    nutrition.food_name or nutrition.food_name_cn or name,
                    nutrition.energy_kcal,
                    nutrition.protein,
                    nutrition.carbohydrate,
                    nutrition.fat,
                    0.95 if is_exact else 0.7
                )
                self._cache[key] = result
                return result
        except Exception as e:
            logger.warning(f"营养数据库查询失败 ({name}): {e}")
        
        # 6. 预定义表关键词匹配（模糊匹配）
        for alias, (db_key, cal, pro, carb, fat) in FOOD_ALIASES.items():
            # 双向包含检查
            if len(key) >= 2 and len(alias) >= 2:
                if key in alias or alias in key:
                    result = FoodMapping(db_key, cal, pro, carb, fat, 0.6)
                    self._cache[key] = result
                    return result
        
        # 7. 分类默认值
        if category and category in CATEGORY_DEFAULTS:
            cal, pro, carb, fat = CATEGORY_DEFAULTS[category]
            result = FoodMapping(f"default_{category}", cal, pro, carb, fat, 0.4)
            self._cache[key] = result
            return result
        
        # 8. 通用默认值
        cal, pro, carb, fat = DEFAULT_NUTRITION
        result = FoodMapping("unknown", cal, pro, carb, fat, 0.3)
        self._cache[key] = result
        logger.info(f"食材 '{name}' 未找到匹配，使用默认值")
        return result
    
    def calculate_nutrition(self, name: str, weight_g: float, category: Optional[str] = None, 
                             cooking_method: Optional[str] = None) -> dict:
        """
        计算食材营养（根据重量和烹饪方法）
        
        Args:
            name: 食材名
            weight_g: 重量（克）
            category: 分类（用于兜底）
            cooking_method: 烹饪方法（用于热量修正）
        
        Returns:
            {"calories": x, "protein": x, "carbs": x, "fat": x, "confidence": x}
        """
        # 先尝试用标准名称查找
        canonical = get_canonical_name(name)
        mapping = self.lookup(canonical, category)
        
        ratio = weight_g / 100
        base_calories = mapping.calories_per_100g * ratio
        base_fat = mapping.fat_per_100g * ratio
        
        # 应用烹饪方法修正
        cooking_multiplier = 1.0
        if cooking_method:
            cooking_multiplier = COOKING_METHOD_MULTIPLIERS.get(
                cooking_method.lower().replace("-", "_"), 1.0
            )
        
        return {
            "calories": round(base_calories * cooking_multiplier, 1),
            "protein": round(mapping.protein_per_100g * ratio, 1),
            "carbs": round(mapping.carbs_per_100g * ratio, 1),
            "fat": round(base_fat * cooking_multiplier, 1),
            "confidence": mapping.confidence,
            "cooking_adjusted": cooking_multiplier != 1.0,
        }


